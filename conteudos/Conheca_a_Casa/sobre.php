        Sobre a Câmara Legislativa
        </legend>
            <p>A Câmara Legislativa do Distrito Federal foi criada após intensa luta pela autonomia política do Distrito Federal. Em 1986, os brasilienses elegeram pela primeira vez seus representantes no Congresso Nacional e, somente em 1990, foram eleitos os primeiros deputados distritais.</p>

<p>As sessões ocorrem ordinariamente de 1º de fevereiro a 30 de junho e de 1º de agosto a 15 de dezembro e, extraordinariamente, nos casos previstos na Lei Orgânica.</p>

<p>As atribuições da Câmara Legislativa foram definidas pela Lei Orgânica do Distrito Federal:</p>

<p>Seção II</p>

<p>Das Atribuições da Câmara Legislativa:</p>


<p>Art. 58. Cabe à Câmara Legislativa, com a sanção do Governador, não exigida esta para o especificado no art. 60 desta Lei Orgânica, dispor sobre todas as matérias de competência do Distrito Federal, especialmente sobre:</p>


<p>I – matéria tributária, observado o disposto nos arts. 145, 147, 150, 152, 155, 156 e 162 da Constituição Federal;</p>
<p>II – plano plurianual, diretrizes orçamentárias, orçamento anual, operações de crédito, dívida pública e empréstimos externos a qualquer título a ser contraídos pelo Distrito Federal;</p>
<p>III – criação, transformação e extinção de cargos, empregos e funções públicas, fixação dos vencimentos ou aumento de sua remuneração;</p>
<p>IV – planos e programas locais de desenvolvimento econômico e social;</p>
<p>V – educação, saúde, previdência, habitação, cultura, ensino, desporto e segurança pública;</p>
<p>VI – autorização para alienação dos bens imóveis do Distrito Federal ou cessão de direitos reais a eles relativos, bem como recebimento, pelo Distrito Federal, de doações com encargo, não se considerando como tais a simples destinação específica do bem;</p>
<p>VII – criação, estruturação e atribuições de Secretarias do Governo do Distrito Federal e demais órgãos e entidades da administração direta e indireta;</p>
<p>VIII – uso do solo rural, observado o disposto nos arts. 184 a 191 da Constituição Federal;</p>
<p>IX – planejamento e controle do uso, parcelamento, ocupação do solo e mudança de destinação de áreas urbanas, observado o disposto nos arts. 182 e 183 da Constituição Federal;</p>
<p>X – criação, incorporação, fusão e desmembramento de Regiões Administrativas;</p>
<p>XI – concessão ou permissão para a exploração de serviços públicos, incluído o de transporte coletivo;</p>
<p>XII – o servidor público, seu regime jurídico, provimento de cargos, estabilidade e aposentadoria;</p>
<p>XIII – criação, transformação, fusão e extinção de entidades públicas do Distrito Federal, bem como normas gerais sobre privatização das entidades de direito privado integrantes da administração indireta;</p>
<p>XIV – prestação de garantia, pelo Distrito Federal, em operação de crédito contratada por suas autarquias, fundações, empresas públicas e sociedades de economia mista;</p>
<p>XV – aquisição, administração, alienação, arrendamento e cessão de bens imóveis do Distrito Federal;</p>
<p>XVI – transferência temporária da sede do Governo;</p>
<p>XVII – proteção e integração de pessoas portadoras de deficiência;</p>
<p>XVIII – proteção à infância, juventude e idosos;</p>
<p>XIX – organização do sistema local de emprego, em consonância com o sistema nacional.</p>
