                <p>Qualquer cidadão pode ter acesso à sede da Câmara Legislativa, bastando, para tanto, se identificar na portaria.</p>

<p>O endereço da Câmara Legislativa é: Praça Municipal - Quadra 2 - lote 5 - Centro Cívico Administrativo – Eixo Monumental – Brasília-DF - CEP: 70094-902. Veja o mapa.</p>

<p>A Casa oferece várias formas de visitas guiadas, mediante agendamento:</p>

<p>Visitas de autoridades, delegações estrangeiras e outras visitas protocolares e institucionais: Cerimonial , 5º Andar - 3348-8271, 3348-8272 – cerimonial@cl.df.gov.br.</p>

<p>Acordo de cooperação técnica com a Câmara dos Deputados inclui a CLDF no roteiro das delegações parlamentares de outros estados que visitam aquela instituição. O mesmo acontece com o Itamaraty, no que diz respeito às delegações estrangeiras. Independente desses convênios, a Câmara Legislativa recebe frequentemente parlamentares e outras autoridades do Brasil e do exterior.</p>

<p>Visitas de estudantes e idosos: Escola do Legislativo (Elegis), 4º Andar - 3348-9205, 3348-8514 - elegisprojesp@cl.df.gov.br - http://www.cl.df.gov.br/elegis.</p>

<p>O programa Conhecendo o Parlamento, da Elegis, tem por objetivo possibilitar o conhecimento do funcionamento do Poder Legislativo e das atividades dos deputados, por meio de palestras com consultores legislativos e de visitas orientadas à CLDF. Atende estudantes do ensino fundamental, do ensino médio e grupos de idosos.</p>

<p>Outras visitas guiadas à sede: Seção de Relações Públicas da Coordenadoria de Comunicação Social, Térreo Inferior - 3348-8286 – serep@cl.df.gov.br.</p>

